    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        String emailFixo1 = "admin@gmail.com";
        String senhaFixa1 = "admin123";
        String tipoFixo1 = "Administrador";

        String email = Email.getText();
        String senha = Senha.getText();

        if (email.equals(emailFixo1) && senha.equals(senhaFixa1)) {
            if (tipoFixo1.equalsIgnoreCase("Administrador")) {
                // Abrir a interface TabelaConta
                TabelaConta tabelaConta = new TabelaConta();
                tabelaConta.setVisible(true);
                dispose();  // Fechar a tela atual, se necess√°rio
            } else {
                // Chamar a tela de menu
                Menuu menu = new Menuu();
                menu.setVisible(true);
                dispose();  // Fechar a tela atual, se necess√°rio
            }
        } else {
            // Verificar se √© um usu√°rio registrado no banco de dados
            try {
                Connection conexao = ConexaoDAO.conectaBD();
                String query = "SELECT tipo_conta FROM usuarios WHERE email = ? AND senha = ?";
                PreparedStatement ps = conexao.prepareStatement(query);
                ps.setString(1, email);
                ps.setString(2, senha);
                ResultSet rs = ps.executeQuery();

                if (rs.next()) {
                    // Verificar o tipo de usu√°rio retornado do banco de dados
                    String tipoUsuario = rs.getString("tipo_conta");
                    if (tipoUsuario.equalsIgnoreCase("funcion√°rio")) {
                        // Abrir a interface TabelaLivros
                        TabelaLivros tabelaLivros = new TabelaLivros();
                        tabelaLivros.setVisible(true);
                        dispose();  // Fechar a tela atual, se necess√°rio
                    } else {
                        // Chamar a tela de menu
                        Menuu menu = new Menuu();
                        menu.setVisible(true);
                        dispose();  // Fechar a tela atual, se necess√°rio
                    }
                } else {
                    // Login inv√°lido
                    JOptionPane.showMessageDialog(null, "Login e/ou senha inv√°lidos.");
                }

                // Fechar recursos
                rs.close();
                ps.close();
                conexao.close();
            } catch (SQLException erro) {
                JOptionPane.showMessageDialog(null, "Erro ao autenticar o usu√°rio: " + erro.getMessage());
            }
        }


    }//GEN-LAST:event_LoginActionPerformed

